package org.acme;

public class Nos {
    double nos;
    double km;

    
    public Nos(double nos, double km) {
        this.nos = nos;
        this.km = km;
    }
    
    public double getNos() {
        return nos;
    }
    public void setNos(double nos) {
        this.nos = nos;
    }
    public double getKm() {
        return km;
    }
    public void setKm(double km) {
        this.km = km;
    }


}
